    private void jTable_locationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_locationMouseClicked
        DefaultTableModel tblModel=(DefaultTableModel)jTable_location.getModel();
        int rowIndex = jTable_location.getSelectedRow();
        TF6Building.setText(tblModel.getValueAt(rowIndex,1).toString());
        TF4RoomName.setText(tblModel.getValueAt(rowIndex,2).toString());
        String RoomType=tblModel.getValueAt(rowIndex,3).toString();
        if(RoomType.equals("Lecture Hall")){
            RBLectureHall.setSelected(true);
            RBLab.setSelected(false);
        }
        else{
            RBLab.setSelected(true);
            RBLectureHall.setSelected(false);
        }
        
        TF5Capacity.setText(tblModel.getValueAt(rowIndex,4).toString());
    }//GEN-LAST:event_jTable_locationMouseClicked

