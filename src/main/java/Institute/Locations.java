    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            DefaultTableModel tblModel = (DefaultTableModel)jTable_location.getModel();
        if(jTable_location.getSelectedRowCount()==1){
              String BuildingName = TF6Building.getText();
              String RoomName    = TF4RoomName.getText();
              String RoomType = " ";
        
               if(RBLectureHall.isSelected()) {
            RoomType = RBLectureHall.getText();
             
         }
            else  {
            RoomType =RBLab.getText();
            }
              
              String Capacity = TF5Capacity.getText();
        
        tblModel.setValueAt(BuildingName, jTable_location.getSelectedRow(), 1);
        tblModel.setValueAt(RoomName, jTable_location.getSelectedRow(), 2);
        tblModel.setValueAt(RoomType, jTable_location.getSelectedRow(), 3);
        tblModel.setValueAt(Capacity, jTable_location.getSelectedRow(), 4);
        
       int row = jTable_location.getSelectedRow();
       Integer ID = (Integer) jTable_location.getModel().getValueAt(row, 0);
       if(TF6Building.getText().equals("") || TF4RoomName.getText().equals("") || (RBLectureHall.isSelected()==false && RBLab.isSelected()==false) || TF5Capacity.getText().equals("") ){
                JOptionPane.showMessageDialog(this,"Please fill all the fields");
          
        
        }
        else{
        location_model.insertUpdateDeleteLocation_Model('u',ID, BuildingName, RoomName, RoomType, Capacity);  
      
        }
        }
         
      
        else
        {
            if(jTable_location.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is empty !!!");
            }else{
                JOptionPane.showMessageDialog(this, "Please Select a Single Row for Update  !!!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

