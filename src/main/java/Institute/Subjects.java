    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         DefaultTableModel tblModel = (DefaultTableModel)jTblManageSubject.getModel();
        if(jTblManageSubject.getSelectedRowCount()==1){
              String OfferedYear = jCbOfferedYear.getSelectedItem().toString();
              String OfferedSemester = " ";
        
               if(jRbSemi1.isSelected()) {
            OfferedSemester = jRbSemi1.getText();
             
         }
            else if(jRbSemi2.isSelected()) {
            OfferedSemester =jRbSemi2.getText();
             
            }
        String SubjectName = jTFSubjectName.getText();
        String SubjectCode = jTFSubjectCode.getText();
        Integer NumberOfLectureHours =  (Integer) jSMLectureHours.getValue();
        Integer NumberOfTutorialHours = (Integer) jSMTutorialHours.getValue();
        Integer NumberOfLabHours = (Integer) jSMLabHours.getValue();
        Integer NumberOfEvaluationHours = (Integer) jSMEvaluationHours.getValue();
        
        tblModel.setValueAt(OfferedYear, jTblManageSubject.getSelectedRow(), 1);
        tblModel.setValueAt(OfferedSemester, jTblManageSubject.getSelectedRow(), 2);
        tblModel.setValueAt(SubjectName, jTblManageSubject.getSelectedRow(), 3);
        tblModel.setValueAt(SubjectCode, jTblManageSubject.getSelectedRow(), 4);
        tblModel.setValueAt(NumberOfLectureHours, jTblManageSubject.getSelectedRow(), 5);
        tblModel.setValueAt(NumberOfTutorialHours, jTblManageSubject.getSelectedRow(), 6);
        tblModel.setValueAt(NumberOfLabHours, jTblManageSubject.getSelectedRow(), 7);
        tblModel.setValueAt(NumberOfEvaluationHours, jTblManageSubject.getSelectedRow(), 8);
        
       int row = jTblManageSubject.getSelectedRow();
       Integer ID = (Integer) jTblManageSubject.getModel().getValueAt(row, 0);
        if(jCbOfferedYear.getSelectedItem().toString().equals("Select") || jRbSemi1.isSelected()==false && jRbSemi2.isSelected()==false || jTFSubjectName.getText().equals("") || jTFSubjectCode.getText().equals("") || jSMLectureHours.getValue().equals(0) || jSMTutorialHours.getValue().equals(0) || jSMLabHours.getValue().equals(0) || jSMEvaluationHours.getValue().equals(0) ){
                JOptionPane.showMessageDialog(this,"Please fill all the fields");
          
        
        }
        else{
        subject_model.insertUpdateDeleteSubject_Model('u',ID, OfferedYear, OfferedSemester, SubjectName, SubjectCode, NumberOfLectureHours, NumberOfTutorialHours, NumberOfLabHours, NumberOfEvaluationHours);  
      
        }
        }
         
      
        else
        {
            if(jTblManageSubject.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is empty !!!");
            }else{
                JOptionPane.showMessageDialog(this, "Please Select a Single Row for Update  !!!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

