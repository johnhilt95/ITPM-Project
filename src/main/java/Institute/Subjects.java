    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
            
            
        
        String OfferedYear = OfferedYearCB.getSelectedItem().toString();
        String OfferedSemester = " ";
        
               if(sem1RB.isSelected()) {
            OfferedSemester = sem1RB.getText();
             
         }
            else if(sem2RB.isSelected()) {
            OfferedSemester =sem2RB.getText();
             
            }
        String SubjectName = SubjectNameTF.getText();
        String SubjectCode = SubjectCodeTF.getText();
        Integer NumberOfLectureHours = (Integer) LecHourSp.getValue();
        Integer NumberOfTutorialHours = (Integer) TuteHourSp.getValue();
        Integer NumberOfLabHours = (Integer) LabHourSp.getValue();
        Integer NumberOfEvaluationHours = (Integer) EvalHourSp.getValue();
       
        
        if(OfferedYearCB.getSelectedItem().toString().equals("Select") || sem1RB.isSelected()==false && sem2RB.isSelected()==false || SubjectNameTF.getText().equals("") || SubjectCodeTF.getText().equals("") || LecHourSp.getValue().equals(0) || TuteHourSp.getValue().equals(0) || LabHourSp.getValue().equals(0) || EvalHourSp.getValue().equals(0) ){
                JOptionPane.showMessageDialog(this,"Please fill all the fields");
          
        
        }else{
        
      
        
        subject_model.insertUpdateDeleteSubject_Model('i',null,OfferedYear,OfferedSemester,SubjectName,SubjectCode,NumberOfLectureHours,NumberOfTutorialHours,NumberOfLabHours,NumberOfEvaluationHours);
        }
        jTblManageSubject.setModel(new DefaultTableModel(null,new Object[]{ID, "Offered_Year","Offered_Semester","Subject_Name","Subject_Code","Number_of_Lecture_Hours","Number_of_Tutorial_Hours","Number_of_Lab_Hours","Number_of_Evaluation_Hours"}));
        
        try{
               Class.forName("com.mysql.jdbc.Driver");
               Connection con = DbConnection.dbconnect();
               DefaultTableModel tblModel = (DefaultTableModel)jTblManageSubject.getModel();
               Statement st=con.createStatement();
               ResultSet rs = st.executeQuery("SELECT * FROM hniti.manage_subjects");
               while(rs.next()){
                   
                    
       
                   Integer ID = rs.getInt("ID");
                    OfferedYear = rs.getString("Offered_Year");
                    OfferedSemester = rs.getString("Offered_Semester");
                   SubjectName = rs.getString("Subject_Name");
                   SubjectCode = rs.getString("Subject_Code");
                   NumberOfLectureHours = rs.getInt("Number_of_Lecture_Hours");
                   NumberOfTutorialHours = rs.getInt("Number_of_Tutorial_Hours");
                   NumberOfLabHours = rs.getInt("Number_of_Lab_Hours");
                   NumberOfEvaluationHours = rs.getInt("Number_of_Evaluation_Hours");
               
                  tblModel.addRow(new Object[]{ID, OfferedYear, OfferedSemester, SubjectName, SubjectCode, NumberOfLectureHours, NumberOfTutorialHours, NumberOfLabHours, NumberOfEvaluationHours});
               }
                 
                    
            
               jTblManageSubject.setModel(tblModel);
            }
               

        catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton14ActionPerformed

